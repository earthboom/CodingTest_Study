//======================================================================================
vector<int> Best_Set(int n, int s)
{
	vector<int> answer;

	if (s < n)
		return { -1 };
	if (n == 1)
		return { s };

	int temp = s / n;
	int remain = s % n;
	answer.assign(n, temp);

	for (int i = 0; i < remain; ++i)
		++answer[(answer.size() - 1) - i];

	return answer;
}
